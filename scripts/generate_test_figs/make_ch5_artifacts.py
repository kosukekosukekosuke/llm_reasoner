_METRICS_RE = re.compile(r"^test_metrics/theta(?P<theta>\d+)/(?P<ds>[^/]+)/(?P<fname>.+metrics_overall\.json)$")
_EXRATE_RE  = re.compile(r"^test_metrics/theta(?P<theta>\d+)/(?P<ds>[^/]+)/(?P<fname>.+exrate_by_status\.csv)$")
_TAG_RE     = re.compile(r"^test_metrics/theta(?P<theta>\d+)/(?P<ds>[^/]+)/(?P<fname>.+tag_breakdown\.csv)$")
_SCEN_RE    = re.compile(r"^test_metrics/theta(?P<theta>\d+)/(?P<ds>[^/]+)/(?P<fname>.+scenario_breakdown\.csv)$")
_LOG_RE     = re.compile(r"^test_log/(?P<ds>[^/]+)_theta(?P<theta>\d+)_run\.jsonl$")


def _safe_div(a: float, b: float) -> float:
    return float(a) / float(b) if b not in (0, 0.0) else float("nan")


def parse_metrics(store: Store) -> pd.DataFrame:
    rows = []
    for rel in store.list():
        m = _METRICS_RE.match(rel)
        if not m:
            continue
        theta = int(m.group("theta"))
        ds = m.group("ds")
        d = json.loads(store.open_text(rel))
        op = d.get("operating_point", {})

        # counts
        in_n_ok = op.get("in_n_ok", 0)
        oom_n_ok = op.get("oom_n_ok", 0)
        in_n_correct_accept = op.get("in_n_correct_accept", 0)
        in_n_accepted = op.get("in_n_accepted", 0)
        in_n_wrong_accept = op.get("in_n_wrong_accept", 0)

        # metrics derived (match thesis definitions)
        acc_in = _safe_div(in_n_correct_accept, in_n_ok)
        coverage = float(op.get("in_coverage", float("nan")))
        selrisk = float(op.get("in_selective_risk", float("nan")))
        abstain = float("nan")
        if not math.isnan(coverage):
            abstain = 1.0 - coverage

        oom_fa = float(op.get("oom_false_accept_rate", float("nan")))
        acc_oom = float(op.get("oom_true_reject_rate", float("nan")))  # correct is reject in OoM set

        ex = d.get("exrate", {}) if isinstance(d.get("exrate", {}), dict) else {}
        ex_total = float(ex.get("exrate_total", float("nan")))
        ex_in = float(ex.get("exrate_in", float("nan")))
        ex_oom = float(ex.get("exrate_oom", float("nan")))

        rows.append({
            "theta": theta,
            "rq": IDX_INFO.get(theta, {}).get("rq", ""),
            "method": IDX_INFO.get(theta, {}).get("name", f"theta{theta}"),
            "method_short": IDX_INFO.get(theta, {}).get("short", f"{theta}"),
            "dataset": ds,
            "acc_in": acc_in,
            "coverage": coverage,
            "abstain": abstain,
            "selrisk": selrisk,
            "oom_fa": oom_fa,
            "acc_oom": acc_oom,
            "exrate_total": ex_total,
            "exrate_in": ex_in,
            "exrate_oom": ex_oom,
            "in_n_ok": in_n_ok,
            "in_n_accepted": in_n_accepted,
            "in_n_wrong_accept": in_n_wrong_accept,
            "oom_n_ok": oom_n_ok,
        })

    df = pd.DataFrame(rows)
    if df.empty:
        raise RuntimeError("metrics_overall.json が見つかりません。入力パスが test_result.zip / 展開ディレクトリか確認してください。")
    # order
    df["dataset"] = pd.Categorical(df["dataset"], categories=DATASET_ORDER, ordered=True)
    df = df.sort_values(["theta", "dataset"]).reset_index(drop=True)
    return df


def parse_breakdown_csv(store: Store, regex: re.Pattern) -> pd.DataFrame:
    rows = []
    for rel in store.list():
        m = regex.match(rel)
        if not m:
            continue
        theta = int(m.group("theta"))
        ds = m.group("ds")
        b = store.open_bytes(rel)
        df = pd.read_csv(io.BytesIO(b))
        df["theta"] = theta
        df["dataset"] = ds
        rows.append(df)
    if not rows:
        return pd.DataFrame()
    out = pd.concat(rows, ignore_index=True)
    out["dataset"] = pd.Categorical(out["dataset"], categories=DATASET_ORDER, ordered=True)
    return out


def parse_logs(store: Store) -> Dict[Tuple[int, str], pd.DataFrame]:
    out: Dict[Tuple[int, str], pd.DataFrame] = {}
    for rel in store.list():
        m = _LOG_RE.match(rel)
        if not m:
            continue
        ds = m.group("ds")
        theta = int(m.group("theta"))
        # read jsonl
        text = store.open_text(rel)
        rows = [json.loads(line) for line in text.splitlines() if line.strip()]
        df = pd.DataFrame(rows)
        out[(theta, ds)] = df
    return out


