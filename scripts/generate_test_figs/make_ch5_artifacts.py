def plot_ablation_heatmap(metrics: pd.DataFrame, out_dir: Path, fmt: str) -> None:
    """
    RQ3向け: 各datasetで (ablation x metric) のΔヒートマップを描く（基準=θ=201）
    """
    ensure_dir(out_dir)
    base = metrics[metrics["theta"] == 201].set_index(["dataset"])
    abls = metrics[metrics["theta"].isin([301, 302, 303, 304, 305])].copy()

    metrics_list = ["acc_in", "abstain", "selrisk", "oom_fa", "acc_oom", "exrate_total"]
    abls_order = [301, 302, 303, 304, 305]

    for ds in DATASET_ORDER:
        mat = []
        labels = []
        for t in abls_order:
            row = abls[(abls["theta"] == t) & (abls["dataset"] == ds)]
            if row.empty:
                continue
            r = row.iloc[0]
            b = base.loc[ds]
            v = []
            for m in metrics_list:
                if pd.isna(r[m]) or pd.isna(b[m]):
                    v.append(np.nan)
                else:
                    v.append(float(r[m]) - float(b[m]))
            mat.append(v)
            labels.append(IDX_INFO.get(t, {}).get("short", str(t)))
        if not mat:
            continue
        A = np.array(mat, dtype=float)
        fig = plt.figure(figsize=(10, 4))
        ax = fig.add_subplot(111)
        im = ax.imshow(A, aspect="auto")
        ax.set_yticks(range(len(labels)))
        ax.set_yticklabels(labels)
        ax.set_xticks(range(len(metrics_list)))
        ax.set_xticklabels(metrics_list, rotation=30, ha="right")
        ax.set_title(f"Δ (ablation - base) for {ds} (base=theta201)")
        fig.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
        save_fig(fig, out_dir / f"ablation_delta_heatmap_{ds}.{fmt}")


