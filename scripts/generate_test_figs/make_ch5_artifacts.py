def build_rq_tables(metrics: pd.DataFrame, out_dir: Path) -> None:
    ensure_dir(out_dir)

    # common columns per dataset type
    cols_in = ["acc_in", "abstain", "selrisk", "exrate_total"]
    cols_oom = ["oom_fa", "acc_oom", "exrate_total"]

    def _format_block(df_sub: pd.DataFrame, cols: List[str]) -> pd.DataFrame:
        x = df_sub[["theta", "method", "dataset"] + cols].copy()
        return x

    # RQ1: 101,102
    rq1 = metrics[metrics["theta"].isin([101, 102])].copy()
    df_to_latex_table(
        _format_block(rq1, cols_in + cols_oom),
        caption="RQ1: LLM-only Baselines の Test 最終評価（主要指標）",
        label="tab:rq1_test_overall",
        out_path=out_dir / "rq1_test_overall.tex",
    )

    # RQ2: 201 vs 101/102
    rq2 = metrics[metrics["theta"].isin([101, 102, 201])].copy()
    df_to_latex_table(
        _format_block(rq2, cols_in + cols_oom),
        caption="RQ2: 提案法 $\\theta^*$ と Baselines の Test 最終評価（主要指標）",
        label="tab:rq2_test_overall",
        out_path=out_dir / "rq2_test_overall.tex",
    )

    # RQ3: 201 vs 301..305 (delta)
    base = metrics[metrics["theta"] == 201].set_index(["dataset"])
    abls = metrics[metrics["theta"].isin([301, 302, 303, 304, 305])].copy()
    rows = []
    for _, r in abls.iterrows():
        ds = r["dataset"]
        b = base.loc[ds]
        for m in ["acc_in", "abstain", "selrisk", "oom_fa", "acc_oom", "exrate_total"]:
            rows.append({
                "theta": int(r["theta"]),
                "method": r["method"],
                "dataset": ds,
                "metric": m,
                "delta": float(r[m]) - float(b[m]) if (not pd.isna(r[m]) and not pd.isna(b[m])) else np.nan,
                "value": float(r[m]),
                "base": float(b[m]),
            })
    ddf = pd.DataFrame(rows)
    # wide delta (metric columns)
    wide = ddf.pivot_table(index=["theta", "method", "dataset"], columns="metric", values="delta").reset_index()
    df_to_latex_table(
        wide,
        caption="RQ3: アブレーションの効果（基準=提案法 $\\theta^*$，差分=各条件−基準）",
        label="tab:rq3_ablation_delta",
        out_path=out_dir / "rq3_ablation_delta.tex",
    )

    # RQ4: map variants 401,402,201
    rq4 = metrics[metrics["theta"].isin([402, 401, 201])].copy()
    df_to_latex_table(
        _format_block(rq4, cols_in + cols_oom),
        caption="RQ4: 地図側意味情報の設計差（$M_{LSD}$ vs $M_{LS}$ vs $M_L$）による Test 最終評価（主要指標）",
        label="tab:rq4_map_variants",
        out_path=out_dir / "rq4_map_variants.tex",
    )

    # Also export a "pretty" wide table: rows=method, cols=(dataset, metric)
    pretty = metrics.copy()
    pretty["row"] = pretty["method_short"]
    pretty = pretty.set_index(["row", "dataset"])[["acc_in", "abstain", "selrisk", "oom_fa", "acc_oom", "exrate_total"]]
    pretty_wide = pretty.unstack("dataset")
    pretty_wide.columns = [f"{c1}_{c2}" for (c1, c2) in pretty_wide.columns]
    pretty_wide = pretty_wide.reset_index()
    pretty_wide.to_csv(out_dir / "all_methods_wide.csv", index=False)


