def ensure_dir(p: Path) -> None:
    p.mkdir(parents=True, exist_ok=True)


def df_to_latex_table(df: pd.DataFrame, caption: str, label: str, out_path: Path, *, float_fmt: str = "%.3f"):
    """
    booktabs形式で LaTeX 表を書き出す（最小限のラッパ）
    """
    latex = df.to_latex(index=False, escape=False, float_format=lambda x: float_fmt % x, longtable=False)
    s = []
    s.append(r"\begin{table}[t]")
    s.append(r"\centering")
    s.append(latex)
    s.append(rf"\caption{{{caption}}}")
    s.append(rf"\label{{{label}}}")
    s.append(r"\end{table}")
    out_path.write_text("\n".join(s) + "\n", encoding="utf-8")


def save_fig(fig, out_path: Path):
    fig.tight_layout()
    fig.savefig(out_path, dpi=200)
    plt.close(fig)


