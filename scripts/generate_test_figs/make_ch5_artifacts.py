def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--input", required=True, help="test_result.zip または展開ディレクトリへのパス")
    ap.add_argument("--out", required=True, help="出力ディレクトリ")
    ap.add_argument("--fig_format", default="png", choices=["png", "pdf"], help="図の拡張子")
    ap.add_argument("--include_heatmap", action="store_true", help="RQ3向けΔヒートマップも生成")
    ap.add_argument("--include_examples", action="store_true", help="代表例抽出（最小限）も生成")
    args = ap.parse_args()

    store = Store(Path(args.input))
    out = Path(args.out)
    ensure_dir(out)

    # 1) metrics
    metrics = parse_metrics(store)
    ensure_dir(out / "summary")
    metrics.to_csv(out / "summary" / "metrics_long.csv", index=False)

    # also wide
    wide = metrics.pivot_table(index=["theta", "method", "method_short"], columns="dataset",
                               values=["acc_in", "abstain", "selrisk", "oom_fa", "acc_oom", "exrate_total"])
    wide.columns = [f"{m}_{ds}" for (m, ds) in wide.columns]
    wide = wide.reset_index()
    wide.to_csv(out / "summary" / "metrics_wide.csv", index=False)

    # 2) tables
    build_rq_tables(metrics, out / "tables")

    # 3) figures
    plot_risk_coverage(metrics, out / "figs", args.fig_format)
    plot_oom_fa(metrics, out / "figs", args.fig_format)
