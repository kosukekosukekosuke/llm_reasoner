# ---- risk_coverage の点ラベル(Prop/cos/noRetry等)が重なるのを避けるためのオフセット(単位: points) ----
# ※追加ライブラリを入れずに、matplotlib標準機能だけで制御するための最小実装。
_RISK_LABEL_OFFSET_PRESET = {
    # 相談で「特に重なる」と言及があったものは固定で散らす
    "RGGS": (4, -4),      # 左上
    "noRetry": (-4, -4), # 左下
    "cos_only": (4, 4),       # 右下
}

# preset以外のラベルは順番にこの候補を回して配置する（右上/右下/左下/左上…を繰り返す）
_RISK_LABEL_OFFSET_CYCLE = [
    (4, 4), (4, -4), (-4, -4), (-4, 4),
    (4, 4), (4, -4), (-4, -4), (-4, 4),
]

def plot_risk_coverage(metrics: pd.DataFrame, out_dir: Path, fmt: str) -> None:
    ensure_dir(out_dir)
    in_datasets = ["test_core", "test_conflict", "test_noisy"]
    for ds in in_datasets:
        sub = metrics[metrics["dataset"] == ds].copy()
        sub = sub.dropna(subset=["coverage", "selrisk"])
        if sub.empty:
            continue

        # θ順に揃えて、ラベル配置の再現性を確保
        if "theta" in sub.columns:
            sub = sub.sort_values("theta")

        fig = plt.figure(figsize=(6.5, 4.0))
        ax = fig.add_subplot(111)
        ax.scatter(sub["coverage"], sub["selrisk"])

        # 点ラベルが重ならないように、ラベルごとにオフセットを変える
        cycle_i = 0
        for _, r in sub.iterrows():
            label = str(r["method_short"])
            x = float(r["coverage"])
            y = float(r["selrisk"])

            if label in _RISK_LABEL_OFFSET_PRESET:
                dx, dy = _RISK_LABEL_OFFSET_PRESET[label]
            else:
                dx, dy = _RISK_LABEL_OFFSET_CYCLE[cycle_i % len(_RISK_LABEL_OFFSET_CYCLE)]
                cycle_i += 1

            ha = "left" if dx >= 0 else "right"
            va = "bottom" if dy >= 0 else "top"

            ax.annotate(
                label,
                (x, y),
                textcoords="offset points",
                xytext=(dx, dy),
                ha=ha,
                va=va,
                fontsize=9,
            )

        ax.set_xlabel("Coverage")
        # ax.set_xlabel("Coverage (in-map accept rate)")
        ax.set_ylabel("SelRisk")
        # ax.set_ylabel("SelRisk (wrong-accept | accept)")

        # 端の点でラベルが図の外に出て切れやすいので、少しだけ余白を追加
        ax.margins(x=0.06, y=0.10)

        ax.grid(True, alpha=0.3)
        save_fig(fig, out_dir / f"risk_coverage_{ds}.{fmt}")



