def extract_examples(logs: Dict[Tuple[int, str], pd.DataFrame], out_dir: Path) -> None:
    """
    各RQにつき、(成功例 / 危険誤受理例 / 有益な棄権例) を1～2件ずつ抽出する（最小限）。
    - ここでは自動抽出のため、厳密な「代表性」は保証しない。
    - 章に載せる際は、人手で最終選定することを推奨。
    """
    ensure_dir(out_dir)

    def pick_one(df: pd.DataFrame, cond) -> Optional[pd.Series]:
        x = df[cond].copy()
        if x.empty:
            return None
        # deterministic: sort by query text if available
        if "q" in x.columns:
            x = x.sort_values("q")
        return x.iloc[0]

    # Use theta201 on core by default
    for ds in ["test_core", "test_conflict", "test_noisy"]:
        key = (201, ds)
        if key not in logs:
            continue
        df = logs[key]
        df_ok = df[df["status"] == "ok"].copy()

        suc = pick_one(df_ok, (df_ok["accept"] == True) & (df_ok["y"] != -1) & (df_ok["y_hat"] == df_ok["y"]))
        dang = pick_one(df_ok, (df_ok["accept"] == True) & (df_ok["y"] != -1) & (df_ok["y_hat"] != df_ok["y"]))
        abst = pick_one(df_ok, (df_ok["accept"] == False) & (df_ok["y"] != -1))

        lines = []
        lines.append(f"# Examples from theta201 ({ds})")
        for title, ex in [("成功例", suc), ("危険誤受理例（WA）", dang), ("有益な棄権例", abst)]:
            lines.append(f"## {title}")
            if ex is None:
                lines.append("(該当なし)")
                continue
            # show minimal fields
            for k in ["id", "q", "y", "y_hat", "accept", "status", "conf", "cos", "combined", "tags", "scenario"]:
                if k in ex.index:
                    lines.append(f"- {k}: {ex[k]}")
        (out_dir / f"examples_theta201_{ds}.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


