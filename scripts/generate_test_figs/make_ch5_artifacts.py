@dataclass
class Store:
    path: Path
    zf: Optional[zipfile.ZipFile] = None

    def __post_init__(self):
        if self.path.is_file() and self.path.suffix.lower() == ".zip":
            self.zf = zipfile.ZipFile(self.path, "r")
        else:
            self.zf = None

    def list(self) -> List[str]:
        if self.zf is not None:
            return self.zf.namelist()
        else:
            return [str(p.relative_to(self.path)).replace("\\", "/") for p in self.path.rglob("*") if p.is_file()]

    def open_text(self, rel: str, encoding: str = "utf-8") -> str:
        if self.zf is not None:
            return self.zf.read(rel).decode(encoding)
        else:
            return (self.path / rel).read_text(encoding=encoding)

    def open_bytes(self, rel: str) -> bytes:
        if self.zf is not None:
            return self.zf.read(rel)
        else:
            return (self.path / rel).read_bytes()

    def close(self):
        if self.zf is not None:
            self.zf.close()


